public class Hostel
{
	long roomno;
	String studentname;
	float studentweight;
	int year;
	
	public void setRoomno(long roomno)
	{
		this.roomno=roomno;
	}
	public void setStudentname(String studentname)
	{
		this.studentname=studentname;
	}
	public void setStudentweight(float studentweight)
	{
		this.studentweight=studentweight;
	}
	public void setYear(int year)
	{
		this.year=year;
	}
	public long getRoomno()
	{
		return roomno;
	}
	public String getStudentname()
	{
		return studentname;
	}
	public float getStudentweight()
	{
		return studentweight;
	}
	public int getYear()
	{
		return year;
	}
}
