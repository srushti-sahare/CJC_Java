public class Car
{
	String name;
	String model;
	String color;
	int year;
	int price;
	Engine eng;
	
	public Car(String name,String model,String color,int year,int price,Engine eng)
	{
		this.name=name;
		this.model=model;
		this.color=color;
		this.year=year;
		this.price=price;
		this.eng=eng;
	}
}

	